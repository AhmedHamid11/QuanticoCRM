// IsCommonPassword checks if a password is in the blocklist of common passwords.
// Returns true if the password should be rejected, false if it's acceptable.
// This check is case-sensitive for exact matches but also checks lowercase version.
func IsCommonPassword(password string) bool {
	// Check exact match first
	if _, exists := commonPasswords[password]; exists {
		return true
	}

	// Also check lowercase version to catch Password vs password
	lower := toLower(password)
	if _, exists := commonPasswords[lower]; exists {
		return true
	}

	return false
}

// toLower converts a string to lowercase without importing strings package
// to keep this package dependency-free
func toLower(s string) string {
	b := make([]byte, len(s))
	for i := range s {
		c := s[i]
		if c >= 'A' && c <= 'Z' {
			c += 'a' - 'A'
		}
		b[i] = c
	}
	return string(b)
}
